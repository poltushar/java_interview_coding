public class swap_3rd_variable {
    public static void main(String[] args) {

        int a = 12, b = 13;
        int c;

        // swap number using third variable

        c = a;
        a = b;
        b = c;

        System.out.println("the swapping value of  is" + "a=" + a + " b=" + b);

    }

}
