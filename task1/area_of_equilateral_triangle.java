package task1;

public class area_of_equilateral_triangle {
    public static void main(String[] args) {

        // area of equilateral triangle=(root 3*a*a)/4

        double area_of_equilateral_triangle;
        double a = 5f;

        area_of_equilateral_triangle = (1.73 * a * a) / 4;
        System.out.println(area_of_equilateral_triangle);

    }

}
