package task1;

public class total_surface_area_of_cyclinder {

    public static void main(String[] args) {

        double total_surface_area_of_cyclinder;
        double radius = 7;
        double height = 13;

        total_surface_area_of_cyclinder = 2 * 3.14 * height + 2 * 3.14 * radius * radius;
        System.out.println(total_surface_area_of_cyclinder);
    }

}
